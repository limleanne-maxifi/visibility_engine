import { notFound } from 'next/navigation';
import { getLeadById } from '@/lib/supabase';
import CopyLinkButton from './CopyLinkButton';

interface Props {
  params: { id: string };
}

export default async function ResultsPage({ params }: Props) {
  const lead = await getLeadById(params.id);

  if (!lead) {
    notFound();
  }

  const plan = { steps: lead.plan_steps, quickWin: lead.plan_quick_win };
  const calendlyUrl = process.env.CALENDLY_URL ?? '#';
  const contactEmail = process.env.MAXIFI_CONTACT_EMAIL ?? 'letsgetstarted@maxifidigital.com';

  return (
    <div className="min-h-screen bg-gray-50 py-10 px-4">
      <div className="max-w-[640px] mx-auto">

        {/* Header */}
        <div className="mb-8">
          <div className="inline-flex items-center gap-1.5 px-3 py-1 rounded-full bg-[#EEEDFE] text-[#3C3489] text-xs font-medium mb-4">
            <span className="w-2 h-2 rounded-full bg-[#534AB7]" />
            Maxifi Digital · AEO Visibility Check
          </div>
          <h1 className="text-3xl font-bold text-gray-900">
            Your AEO action plan, {lead.first_name}
          </h1>
          <p className="mt-1 text-gray-500">
            Tailored for a {lead.occupation} in {lead.industry}
          </p>
        </div>

        {/* Steps */}
        <div className="space-y-4 mb-6">
          {plan.steps.map((step) => (
            <div
              key={step.num}
              className="bg-white rounded-xl border border-gray-100 shadow-sm p-6"
            >
              <div className="inline-block text-xs font-semibold text-[#534AB7] bg-[#EEEDFE] px-2 py-0.5 rounded mb-2">
                Step {step.num}
              </div>
              <h2 className="text-lg font-semibold text-gray-900 mb-2">{step.title}</h2>
              <p className="text-gray-600 text-sm leading-relaxed">{step.body}</p>
            </div>
          ))}
        </div>

        {/* Quick Win callout */}
        <div className="bg-emerald-50 border border-emerald-200 rounded-xl p-6 mb-8">
          <div className="flex items-center gap-2 mb-2">
            <span className="text-emerald-600 text-lg">⚡</span>
            <span className="text-xs font-semibold text-emerald-700 uppercase tracking-wide">
              Your biggest quick win today
            </span>
          </div>
          <p className="text-emerald-900 text-sm font-medium leading-relaxed">{plan.quickWin}</p>
        </div>

        {/* CTA block */}
        <div className="bg-white rounded-xl border border-gray-100 shadow-sm p-6 mb-8">
          <h2 className="text-xl font-bold text-gray-900 mb-2">
            Want Maxifi Digital to do this for you?
          </h2>
          <p className="text-gray-500 text-sm mb-5">
            We implement AEO strategies for professionals and businesses. Most clients start seeing results in 30–60 days.
          </p>
          <div className="flex flex-col sm:flex-row gap-3">
            <a
              href={calendlyUrl}
              target="_blank"
              rel="noopener noreferrer"
              className="flex-1 text-center py-3 px-5 bg-[#534AB7] hover:bg-[#4640a0] text-white text-sm font-semibold rounded-lg transition-colors"
            >
              Book a free 30-min AEO audit →
            </a>
            <CopyLinkButton />
          </div>
        </div>

        {/* Footer */}
        <p className="text-center text-xs text-gray-400">
          Plan generated by{' '}
          <a
            href="https://maxifidigital.com"
            target="_blank"
            rel="noopener noreferrer"
            className="underline hover:text-gray-600"
          >
            Maxifi Digital
          </a>
          {' · '}Results may vary · {' '}
          <a href={`mailto:${contactEmail}`} className="underline hover:text-gray-600">
            Unsubscribe from emails anytime
          </a>
        </p>
      </div>
    </div>
  );
}
